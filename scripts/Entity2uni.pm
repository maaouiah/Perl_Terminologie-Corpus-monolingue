# Déclaration de la fonction de conversion des entités
# But :
# Environnement
# 	déclaration globale de %entity2uni avec ses données
# Entrées : 
#	arg1 : string à convertir
#	arg2 : booléen xml. Si =1, on ne remplace pas &amp; &lt &gt
# Sorties :
#	valeur de retour : string convertie avec les car. correspondants

sub convertEntities {
	my $string=shift(@_); # lecture du premier argument dans la variable $string
	my $xml=shift(@_);
	my $result="";
	
	while ($string=~/^(.*?)(&#?\w+;)(.*)/s) {
		my $before= $1;
		my $entity= $2;
		my $after= $3;
		
		# on ajoute ce qui précède
		$result.=$before; # $result=$result.$before;
		
		# conversion de l'entité p.ex. "&circ;"
		my $char=$entity;
		
		if ($entity !~/&(amp|lt|gt);/ || !$xml) {
			# 1/ on cherche la valeur unicode hexadécimale ou décimale correspondante 
			if (exists($entity2uni{$entity})) {
				my $uni=$entity2uni{$entity}; # p.ex. "00EA"
				$char=chr(hex($uni)); # p.ex. "ê"
			} elsif ($entity=~/&#x([a-f\d]*);/i) {
				my $uni=$1; # p.ex. "00EA"
				$char=chr(hex($uni)); # p.ex. "ê"
			} elsif ($entity=~/&#(\d+);/) {
				$char=chr($1);
			}
		}
				
		# on ajoute le caractère converti
		$result.=$char;
		
		# on prépare la prochaine itération en réduisant $string à ce qui suit
		$string=$after;
		
	}
	# exemple de conversion $text =~s/&ecirc;/\N{U+00EA}/g;
	# on ajoute le reste à $result
	$result.=$string;
	
	return $result;
}


